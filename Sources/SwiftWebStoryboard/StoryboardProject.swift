    private var markerFile: URL {
        projectDirectory.appendingPathComponent(".swiftweb-storyboard")
    }

    func materialize(force: Bool) throws {
        let fileManager = FileManager.default
        if fileManager.fileExists(atPath: projectDirectory.path) {
            let isManagedDirectory = fileManager.fileExists(atPath: markerFile.path)
            if !force, !isManagedDirectory {
                throw StoryboardError.storyboardDirectoryNotManaged(projectDirectory)
            }
            if force {
                try fileManager.removeItem(at: projectDirectory)
            } else if isManagedDirectory {
                try removeOwnedGeneratedFiles()
            }
        }

        try fileManager.createDirectory(at: projectDirectory, withIntermediateDirectories: true)
        for file in files {
            try write(file)
        }
    }

    private func removeOwnedGeneratedFiles() throws {
        let fileManager = FileManager.default
        let paths = [
            "Package.swift",
            "README.md",
            "Sources",
        ]
        for path in paths {
            let url = projectDirectory.appendingPathComponent(path)
            if fileManager.fileExists(atPath: url.path) {
                try fileManager.removeItem(at: url)
            }
        }
    }

    private var files: [TemplateFile] {
        [
            TemplateFile(path: ".swiftweb-storyboard", contents: "managed by swift-web\n"),
            TemplateFile(path: "Package.swift", contents: packageSwift),
            TemplateFile(path: "README.md", contents: readme),
            TemplateFile(path: "Sources/SwiftWebStoryboard/App.swift", contents: appSwift),
            TemplateFile(path: "Sources/SwiftWebStoryboard/Routes/StoryboardPage.swift", contents: storyboardPageSwift),
            TemplateFile(path: "Sources/SwiftWebStoryboard/Components/StoryboardCatalog.swift", contents: storyboardCatalogSwift),
        ]
    }

    private func write(_ file: TemplateFile) throws {
        let url = projectDirectory.appendingPathComponent(file.path)
        try FileManager.default.createDirectory(
            at: url.deletingLastPathComponent(),
            withIntermediateDirectories: true
        )
        try file.contents.write(to: url, atomically: true, encoding: .utf8)
    }

